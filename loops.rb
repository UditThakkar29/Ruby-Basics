# While loop

x = 5
while x>=2
  puts x
  x = x-1
end

# FOR LOOP

for a in 1..4 do
  puts a
end

arr = [1,3,4,"array","hash","range"]
for i in arr do
  puts i
end

# DO WHILE LOOP

loop do
  puts "Hello"
  val = 10
  if val == 10
    break
  end
end

num = 2
until num == 5 do
  puts num
  num = num + 1
end
